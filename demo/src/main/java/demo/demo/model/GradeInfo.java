package demo.demo.model;

public class GradeInfo {
    String xh;
    String kh;
    String km;
    int xf;
    int pscj;
    int kscj;
    int zpcj;

    public String getKh() {
        return kh;
    }

    public void setKh(String kh) {
        this.kh = kh;
    }

    public String getKm() {
        return km;
    }

    public void setKm(String km) {
        this.km = km;
    }

    public int getXf() {
        return xf;
    }

    public void setXf(int xf) {
        this.xf = xf;
    }

    public int getPscj() {
        return pscj;
    }

    public void setPscj(int pscj) {
        this.pscj = pscj;
    }

    public int getKscj() {
        return kscj;
    }

    public void setKscj(int kscj) {
        this.kscj = kscj;
    }

    public int getZpcj() {
        return zpcj;
    }

    public void setZpcj(int zpcj) {
        this.zpcj = zpcj;
    }
}
